# Função para verificar quantos commits estão no CSV
def verificar_commits(csv_file, commits):
    # Contador de commits encontrados
    commits_encontrados = 0

    with open(csv_file, mode="r") as file:
        # Lê o arquivo CSV
        reader = csv.DictReader(file)

        # Percorre cada linha no CSV
        for row in reader:
            # Verifica se o commit está na lista
            if row["Commit Hash"] in commits:
                commits_encontrados += 1

    return commits_encontrados


